/*OPERADORES DE COMPARAÇÃO 
> - Maior que
>= - Maior ou igual a
< - Menor que
<= - Menor ou igual a
== - Igual a
=== - Estritamente igual a
!= - Diferente de
!== - Estritamente diferente de
*/

console.log(10 > 5);
console.log(28 > 30);
console.log(9 >= 8);
console.log(7 >= 19);
console.log(7 < 11);
console.log(98 < 11);
console.log(2 <= 29);
console.log(56 <= 30);
console.log(5 == '5');
console.log(5 == 8);
console.log(5 === 5);
console.log(5 === '5');
console.log(15 != 3);
console.log(15 != 15);
console.log(10 !== '10');
console.log(10 !== 10);

